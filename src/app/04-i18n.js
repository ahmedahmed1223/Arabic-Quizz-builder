  let _currentLang='ar';

  function t(key,vars,fallback){
    let str=(_translations[_currentLang]&&_translations[_currentLang][key])||(_translations.ar[key]);
    if(!str){
      // If vars is a string, treat it as the fallback (legacy calling convention).
      // If vars is an object and a third fallback arg is provided, use that.
      if(typeof vars==='string'){str=vars;}
      else if(typeof fallback==='string'){str=fallback;}
      else{str=key;}
    }
    if(vars&&typeof vars==='object'){
      Object.keys(vars).forEach(k=>{str=str.replace(new RegExp('\\{'+k+'\\}','g'),vars[k]);});
    }
    return str;
  }

  function setLang(langCode){
    if(!_translations[langCode])return;
    _currentLang=langCode;
    // Persist in state
    try{
      if(typeof state!=='undefined'){
        state.settings.language=langCode;
        if(typeof saveState==='function')saveState();
      }
    }catch(e){console.error("[Error]",e);}
    // Update dir/lang on html
    const isRTL=langCode==='ar';
    document.documentElement.dir=isRTL?'rtl':'ltr';
    document.documentElement.lang=langCode;
    document.body.classList.toggle('dir-ltr',!isRTL);
    document.body.classList.toggle('dir-rtl',isRTL);
    // Update page title
    try{document.title=t('app.title')+' — '+(state?.settings?.name||'');}catch(e){console.error("[Error]",e);}
    // Apply all data-i18n elements
    apply();
    // Notify app
    if(typeof onLanguageChange==='function')onLanguageChange(langCode);
  }

  function getCurrentLang(){return _currentLang;}

  function apply(){
    document.querySelectorAll('[data-i18n]').forEach(el=>{
      const keyStr=el.getAttribute('data-i18n');
      const attrStr=el.getAttribute('data-i18n-attr');
      const varsAttr=el.getAttribute('data-i18n-vars');
      let vars=null;
      if(varsAttr){
        try{vars=JSON.parse(varsAttr);}catch(e){console.error("[Error]",e);}
      }
      // Support comma-separated keys and attrs for multiple translations on one element
      const keys=keyStr.split(',').map(k=>k.trim());
      const attrs=attrStr?attrStr.split(',').map(a=>a.trim()):[];
      keys.forEach((key,i)=>{
        const translated=t(key,vars);
        const attr=attrs[i]||null;
        if(attr==='placeholder'){
          el.placeholder=translated;
        }else if(attr==='title'){
          el.title=translated;
        }else if(attr==='aria-label'){
          el.setAttribute('aria-label',translated);
        }else if(!attr){
          el.textContent=translated;
        }
      });
    });
    // Update language switcher buttons
    document.querySelectorAll('.i18n-lang-label').forEach(el=>{
      el.textContent=_currentLang==='ar'?'English':'العربية';
    });
    // Update lang-selector dual-button active states
    document.querySelectorAll('.lang-selector button').forEach(btn=>{
      var btnLang=btn.id==='lang-btn-ar'?'ar':(btn.id==='lang-btn-en'?'en':btn.getAttribute('data-lang'));
      btn.classList.toggle('active',btnLang===_currentLang);
    });
  }

